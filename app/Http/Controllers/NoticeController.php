     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, Hospital $hospital)
    {
        $inputs = request()->validate([
            'content' => 'required',
        ]);

        $inputs['hospital_id'] = $hospital->id;
